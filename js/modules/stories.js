function getFunFact(category) {
    const cat = funFacts.find(f => f.category === category);
    if (cat && cat.facts.length > 0) {
        return cat.facts[Math.floor(Math.random() * cat.facts.length)];
    }
    return funFacts[0].facts[0];
}

function getRandomFunFact() {
    const randomCat = funFacts[Math.floor(Math.random() * funFacts.length)];
    return {
        category: randomCat.category,
        fact: randomCat.facts[Math.floor(Math.random() * randomCat.facts.length)]
    };
}

